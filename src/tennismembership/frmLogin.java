    /**A method that returns a query that gets details of a member 
     * from the member table in the database
     */
    private String mMemberValidationQuery()
    {
        return "SELECT MemberID, Name, Password, Membership FROM tblMembers"
                + " WHERE Name ='"+txtUsername.getText()+"'AND password ='"+txtPassword.getText()+"'";
    }
    
    /**A method that returns a query that gets details of a consultant 
     * from the consultant table in the database
     */
    private String mConsultantValidationQuery()
    {
        return "SELECT ConsultantID, Name, Password, Rank FROM tblConsultants"
                + " WHERE Name ='"+txtUsername.getText()+"'AND Password ='"+txtPassword.getText()+"'";
    }
    /** A method that fetches details from the database.
     * From which table depends on the query passed as a parameter
     * @param strQuery 
     */
    private void mFetchMemberDetails(String strQuery)
    {
        Statement stStatement = null;
        ResultSet rs;
        clsConnect.mConnectToDatabaseTennisClub();
        try
        {
            stStatement = clsConnect.mConnectToDatabaseTennisClub().prepareStatement(strQuery);
            stStatement.execute(strQuery);
            rs = stStatement.getResultSet();
            while(rs.next())
            {
                intUserID = rs.getInt(1);
                strUserName = rs.getString(2);
                strUserPassword = rs.getString(3);
                strUserRank = rs.getString(4);
            }
            stStatement.close();
            rs.close();
        }
        catch(SQLException eX)
        {
            JOptionPane.showMessageDialog(null, "The application has run to a technical error\n"+eX);
        }
    }
