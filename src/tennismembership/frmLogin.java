    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        /** An instance of frmMainWindow is instantiated
         * User details passed to the login form input controls are checked
         * if they are equal to the given strings below.
         * If the are equal, the user is granted Senior Consultant rights.
         * The string variable strUserIdentifier is given a value, the instance
         * given a title and shown
         * If the input values are not equal the given strings, the input values
         * are checked against the ones fetched from the database. If they are equal 
         * the user is granted rights then access to the application or an appropriate 
         * error is shown         *
         */
        frmMainWindow frmMain = new frmMainWindow();
        if(txtUsername.getText().toLowerCase().equals("sanele") && txtPassword.getText().toLowerCase().equals("56001174")
                && cboUserStatus.getSelectedItem().toString().toLowerCase().equals("senior consultant"))
        {
            frmMainWindow.strUserStatus = "SENIOR CONSULTANT";
            frmMain.mSetStatusRights();
            strUserIdendifier = "Consultant";
            frmMain.setTitle("Tennis Club");
            frmMain.setVisible(true);
            this.setVisible(false);
            frmMain.setExtendedState(MAXIMIZED_BOTH);
        }
        else
        {
            try
            {    
                if(cboUserStatus.getSelectedItem().toString().endsWith("Consultant"))
                {
                    mFetchMemberDetails(mConsultantValidationQuery());
                }
                else
                {
                    mFetchMemberDetails(mMemberValidationQuery());
                }
                if(strUserRank.endsWith("Membership"))
                {
                    strUserIdendifier = "Member";
                }
                else if(strUserRank.endsWith("Consultant"))
                {
                    strUserIdendifier = "Consultant";
                }
                if(strUserName.toUpperCase().equals(txtUsername.getText().toUpperCase()) &&
                        strUserPassword.toUpperCase().equals(txtPassword.getText().toUpperCase())
                        && strUserRank.toUpperCase().equals(cboUserStatus.getSelectedItem().toString().toUpperCase()))
                    {
                    frmMainWindow.strUserStatus = cboUserStatus.getSelectedItem().toString().toUpperCase();
                    frmMain.mSetStatusRights();
                    frmMain.setTitle("Tennis Club");
                    frmMain.setVisible(true);
                    this.setVisible(false);
                    frmMain.setExtendedState(MAXIMIZED_BOTH);
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "ACCESS DENIED!! Incorrect credentials.");
                }
            }
            catch(Exception eX)
            {
                JOptionPane.showMessageDialog(null, "ACCESS DENIED!! Check Username, Password, Login as membership."
                        + "\nOr if you have subscribed your membership has not been approved yet!");
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

